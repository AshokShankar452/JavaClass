package Loops;

public class Nestedfor {

	public static void main(String[] args) {
		int i,j;
		
		System.out.println("loop started");
		for(i=0;i<=3;i++) {
			System.out.println("i loop started");
			System.out.println(i);
			for (j=0;j<=2;j++) {
				System.out.println(j);
				
			}
			
		}
		
	}

}
